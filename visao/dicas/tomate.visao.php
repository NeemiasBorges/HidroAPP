      <li>Os tomateiros são cultivados no mundo todo, mas não suportam extremos de temperatura, ou seja, não crescem bem nem em baixas temperaturas (temperaturas diurnas abaixo de 15°C), que prejudicam o crescimento da planta e diminuem a taxa de germinação das sementes, nem em altas temperaturas (acima de 27°C), que podem prejudicar a formação dos frutos. Geralmente o tomateiro cresce melhor com temperaturas diurnas entre 20°C e 26°C, com uma variação de temperatura entre o dia e a noite. </li>
      <li>Os tomateiros geralmente crescem e produzem melhor em condições de alta luminosidade, com sol direto pelo menos algumas horas por dia.</li>
      <li>Os melhores resultados são obtidos em um solo bem drenado, fértil e rico em matéria orgânica, com pH entre 5,5 e 7.</li>
