    <ul>
      <li>Os feijoeiros são plantas que se desenvolvem bem em ambientes quentes, logo, para que cresçam com vigor é importante manter a temperatura em torno de 15°C e 30°C, no entanto, a temperatura ideal, é entre 18°C e 25°C. O feijoeiro não suporta temperaturas baixas nem geadas.</li>
      <li>A alta luminosidade faz com que os feijões se desenvolvam e sequem com maior facilidade, sendo uma das principais exigências para uma plantação de qualidade.</li>
      <li>Limpe dentro do cano com cloro e agua</li>
      <li>pH entre 6,5 e 7 e ter boa fertilidades, com os nutrientes em equilíbrio, como Nitrogênio, Fósforo e Potássio –  NPK. Além disso, é preciso que haja boa drenagem da água, promovendo à raiz um solo ligeiramente úmido, mas não encharcados.</li>
