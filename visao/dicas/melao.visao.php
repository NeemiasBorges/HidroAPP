      <li>A temperatura ideal para o cultivo de melão situa-se entre 25°C e 32°C, embora possa crescer em temperaturas entre 18°C e 35°C.</li>
      <li>Durante a maturação dos frutos, o ideal é um clima quente e seco. Em condições de alta umidade do solo e do ar, os melões ficam com menor teor de açúcar e ficam mais aguados, perdendo em sabor em relação àqueles cultivados nas condições ideais de temperatura e umidade. </li>
      <li>O meloeiro necessita de alta luminosidade, com luz solar direta.</li>
      <li>O solo deve ser bem drenado, leve, fértil e rico em matéria orgânica. O pH ideal do solo situa-se entre 6,4 e 7,2.</li>
