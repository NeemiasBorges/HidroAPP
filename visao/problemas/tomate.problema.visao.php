      <ul>
        <li><b>Mela-de-rizoctonia</b>
          A doença manifesta-se durante a floração, formação e maturação dos frutos, quando é maior a cobertura foliar.
           As folhas e hastes infectadas apresentam
          podridão mole e aquosa (mela), principalmente nas partes que ficam em contato com o solo. Os frutos doentes
          apresentam podridão marrom, mole e aquosa, coberta por um mofo marrom-claro .
          Para o manejo, recomenda-se: não plantar em solos compactados ou sujeitos a encharcamentos, evitar locais e épocas
           favoráveis à doença, adotar uma densidade adequada de plantas, plantar preferencialmente cultivares mais eretas e controlar a irrigação,
           principalmente durante as fases de floração e frutificação.
        </li>
        <li><b>Murcha-de-fusário</b>
          Plantas com esta doença apresentam murcha das folhas superiores, principalmente
          nas horas mais quentes do dia. As folhas mais velhas tornam-se amareladas e,
          muitas vezes, observa-se murcha ou amarelecimento em apenas um lado da planta ou
          da folha (Figura 3). Os frutos não se desenvolvem, amadurecem ainda
          pequenos e a produção é reduzida. Ao cortar o caule próximo às raízes, verifica-se necrose
          do sistema vascular (Figura 4). Temperatura alta (em torno de 28 °C), solos arenosos com pH baixo
          e o ataque de nematóides favorecem a doença. O fungo sobrevive no solo por períodos superiores a sete anos,
          principalmente por meio de microescleródios (estrutura de resistência do fungo). Como medidas de manejo e controle,
          recomenda-se: plantar cultivares com resistência às raças do patógeno, evitar o plantio em áreas sabidamente infestadas pelo
          fungo e/ou por nematóides patogênicos ao tomateiro e fazer rotação de cultura com gramíneas.
      </li><li><b>Murcha-de-verticílio</b>
        </li>O sintoma inicial desta doença é a murcha suave e parcial da planta nas horas mais quentes do dia (Figura 5).
        As folhas mais velhas tornam-se amareladas e necrosadas nas bordas, em forma de "V" invertido. Os frutos ficam pequenos
        e mal formados. Na região do colo do caule, verifica-se leve necrose vascular, não tão intensa quanto a causada por F.
        oxysporum f. sp. lycopersici. O fungo é bem adaptado a regiões de solos neutros ou alcalinos e com temperaturas amenas
        (20 a 24 °C). No entanto, já foi relatada sua ocorrência no Estado de Pernambuco, onde as temperaturas médias são comumente elevadas.
        O fungo sobrevive no solo por mais de oito anos por meio de microescleródios e infecta mais de 200 hospedeiras. Como medidas de manejo,
        recomenda-se plantar cultivares resistentes e fazer rotação da cultura com gramíneas.
        <li><b>Pinta-preta<b>
     </li>Esta doença afeta toda a parte aérea da planta, a partir das folhas mais velhas e próximas ao solo. Na folha,
      a doença caracteriza-se pela presença de manchas grandes, escuras,
      circulares, com anéis concêntricos (Figura 6). O ataque severo provoca
      desfolha acentuada e expõe o fruto à queima de sol. Também é comum o aparecimento
      de cancro no colo, nas hastes (Figura 7) e nos frutos (Figura 8). A doença é favorecida por
      temperatura elevada (24 a 34 °C) e umidade alta. O fungo sobrevive nos restos culturais e infecta
      outras hortaliças como a batata e a berinjela, além de plantas invasoras como o juá-de-capote. A doença
      é também transmitida por sementes.Não existem cultivares comerciais resistentes. Deve-se pulverizar preventivamente com os fungicidas
       registrados para essa doença. Recomenda-se, também, incorporar os restos culturais imediatamente após a última colheita e fazer rotação de cultura com gramíneas.
       <li><b>Podridão-de-esclerócio<b>
         Plantas doentes apresentam uma podridão mole e aquosa, principalmente nas folhas, hastes e frutos,
          que ficam em contato direto com o solo (Figura 9). Em condições de alta umidade,
         há um crescimento micelial muito vigoroso, de cor branca, semelhante a fios de algodão, na superfície dos
          tecidos afetados. Algumas vezes esse micélio se desenvolve na superfície do solo, próximo à planta. Também é
           comum a formação de pequenos grânulos de cor marrom-clara (escleródios) na superfície dos tecidos afetados.
           O escleródio é uma forma de sobrevivência do fungo no solo por vários anos. A incidência da doença é maior em períodos quentes
          (30 a 35 °C) e chuvosos, em lavouras conduzidas em solos muito argilosos e/ou compactados, com encharcamento do solo. Excesso de folhas,
          solo úmido e contato do fruto com o solo favorecem a doença.
      </li>
      </ul>
