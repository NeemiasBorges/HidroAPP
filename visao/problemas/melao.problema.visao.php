     <ul>
       <li><b>Cancro da haste (podridão ou micosferela)</b>
         É causado por um fungo do solo. Infecta as
          hastes a partir da região do colo da planta, na forma de rachadura; às vezes ocorre nas folhas também. Esse fungo pode ser transmitido via sementes.
         Controle: usar sementes sadias; procurar solos de boa drenagem; fazer plantio em áreas onde
         a doença não ocorreu ou em áreas de rotação; não fazer a amontoa, pois dificulta as observações
         iniciais; se a irrigação for por sulco, evitar fazer o plantio à linha d'água;
         fazer queima dos restos da cultura; usar rotação de culturas com espécies não hospedeiras do patógeno
         e eliminar plantas muito atacadas e pulverizar com fungicidas.
       </li>

       <li><b>Oídio</b>
         É também conhecido por cinza. É muito encontrado na superfície das folhas; inicia na parte inferior
         com um crescimento branco pulverulento. Com o desenvolvimento do fungo, a área afetada fica amarelada e ocorre, enfim, uma necrose. Os frutos jovens, se forem atacados, ficarão deformados.
         O ataque se inicia nas folhas mais velhas, e em ataques severos, as folhas e ramas jovens podem morrer.
         Se usar um espaçamento não muito adensado, irá ajudar no controle da doença. Em condições de temperaturas e umidade relativa elevada, sem chuva, vai favorecer ao fungo.
         Controle: ao escolher a área de plantio, observar a posição do vento, pois o fungo é disseminado muito facilmente pelo
         vento em áreas com plantio mais antigo, a favor do vento; servem como fonte de inócuo para os cultivos novos; reduzir a densidade de plantio;
         alterar os produtos fúngicos; eliminar os restos culturais da área; retirar da área todas as plantas velhas (remanescentes) e as cucurbitáceas nativas, pois são plantas hospedeiras;
         usar rotação de culturas e pulverizar com fungicidas sistêmicos alternados (Fenarimol e Pirazofos) com intervalo de 10 dias.

     </li>

     <li><b>Míldio</b>
Condições de umidade alta e
livre disponibilidade de água facilitam sua disseminação. O
fungo ataca as folhas; inicia-se como áreas de tecido encharcado;
em seguida, ficam necróticas, com cor telha; inicialmente, são pequenas e depois ficam grandes;
são limitadas pelas nervuras e de formato anguloso. Alto ataque da doença resulta em desfolhamento precoce e, com isso,
crescimento retardado da planta. Chuvas leves e temperaturas de 22ºC, aproximadamente, são fatores favoráveis.
Controle: escolher as áreas altas, e não de baixadas, com possibilidade de acumular ar frio e mal ventiladas;
evitar os períodos sujeitos às neblinas e temperaturas favoráveis ao desenvolvimento do fungo; importante observar a posição do vento;
procurar usar espaçamentos maiores; eliminar os restos da cultura; erradicar as plantas muito atacadas e pulverizações com fungicidas.
Aprimore seus conhecimentos.
     </li>
