     <ul>
       <li><b>Cigarrinha</b>
São verdes, chupam a seiva das plantas, ainda transmitindo-lhes várias doenças como os mosaicos, por exemplo.
 Ela ataca a plantação principalmente em épocas mais secas ou em plantio da seca.
 Para combatê-la é necessário a utilização de inseticidas sistêmicos, até a floração.
       </li>

       <li><b>Tripes</b>
É um inseto preto com 2 faixas brancas nas asas,
fica alojado sempre na parte de baixo da folha, provocando
 o desfolhamento da planta. Para acabar com os tripes é feito o uso de inseticidas.
     </li>

     <li><b>Bacteriose</b>O sinal principal da bacteriose são manchas arredondadas que aparecem nas folhas, cujo centro fica seco.
        Altas temperaturas e umidade do ar
         deixam o feijão mais suscetível ao aparecimento
          e evolução dessa doença. Para combatê-la deve-se fazer rotação de culturas e plantar variedades resistentes a ela
     </li>

    <li><b>Ferrugem</b>
      Como muitos já sabem, a ferrugem afeta em especial as folhas, sob a forma inicial de manchas amarelas que depois passam
       a ser vermelho-ferrugem. Temperaturas maiores ou superiores
      a 25ºC e muita umidade do ar, favorecem o desenvolvimento da doença. Plantar variedades
      resistentes, rotação de culturas e fungicidas, são as medidas indicadas para eliminá-la.
    </li>
     </ul>
