     <ul>
       <li><b>Mancha de cercospora:</b>
       causada pelo fungo Cercospora longíssima.
       É uma doença de parte aérea e os principais sintomas são: presença de manchas circulares,
       de coloração parda com bordas bem definidas e centro mais claro. Incide primeiramente nas folhas mais velhas.
       Com o avanço da doença estas manchas se aproximam umas das outras (coalescem), danificando grande parte do limbo foliar
       Controle: Plantio de sementes e mudas sadias e de boa qualidade, plantio em solos bem drenados e que não acumulem água.
       Para permitir uma melhor ventilação, aumentar o espaçamento em períodos quentes, evitar plantios próximos a
       lavouras velhas, irrigar de preferência por gotejamento, eliminar restos culturais e realizar rotação de cultura.
       </li>
       <li><b>Septoriose:</b>
       Assim como a mancha de Cercospora, a
       Septoriose tambem é uma doença de parte aérea e ataca principalmente as folhas,
       apresentando manchas com contornos irregulares. No tecido afetado, é possível observar um aspecto desidratado,
       de coloração parda e com pontos escuros visíveis a olho nu, que são as estruturas do patógeno (figura 2).
       Controle: Os métodos de controle para essa doença são os mesmos da mancha de cercospora citados anteriormente.
     </li><li><b>Podridão-mole:</b>
       Essa doença causa murcha e morte das plantas, quando retirada do solo a planta apresenta intensa podridão mole na região da coroa,
        podendo a lesão se estender e causar a necrose de toda a planta, que emite um odor desagradável e característico (figura 3).
       Controle: Como medida de controle, o recomendado é utilizar substrato livre de patógenos, plantio de mudas sadias em
       solo bem drenado, aumentar o espaçamento de plantio em períodos quentes, irrigar preferencialmente por gotejamento,
       eliminar restos culturais e promover a rotação de cultura.</li>
       <li><b>Murcha-de-fusário:</b>
       Doença que ataca o caule, limitando o
       desenvolvimento da planta, promovendo o amarelecimento e a
       murcha das folhas. Ao cortar longitudinalmente o caule, pode-se
       verificar o escurecimento do xilema
       Controle: Recomenda-se o plantio de variedades resistentes, plantio de sementes certificadas e em
       solos drenados, utilização de substrato livre de patógenos, evitar irrigação excessiva e umidade,
       utilizar agua de boa qualidade e evitar fermentos nas plantas, eliminar as plantas doentes, promover a
      rotação de culturas, utilizar material orgânico esterilizado nos canteiros e limpar o maquinário antes das atividades.
    </li><li><b>Murcha-de-esclerócio:</b>
       Os sintomas dessa doença são semelhantes
       aos apresentados pela podridão-de-esclerotínia. São
        formados microescleródios menores, com formato irregular
        e de coloração marrom ou preto, similar a grãos de pólvora
       Controle: As mesmas recomendações feitas para a murcha-de-fusário são validas e devem ser aplicadas como medida de controle para a murcha-de-esclerócio.
     </li><li><b>Nematoide-das-galhas:</b>
       Doença que ataca as raízes, tendo como sintoma mais característico a
        presença de galhas e inchaços nas raízes. Geralmente, as raízes infectadas
        são mais curtas e com menor número de raízes laterais

       Como reflexo, pode surgir sintomas na parte aérea, como:
       amarelecimento, nanismo, cabeças de alface menores e mais leves,
       folhas mais soltas e murchas. É possível observar também, massas
       de ovos como pontos mais escuros na superfície das raízes galhadas.
       Controle: Como medidas gerais para controle, é recomendado o plantio de cultivares resistentes,
        eliminação de plantas voluntarias da mesma família do alface, eliminar plantas doentes, solarização
        (método de desinfestação do solo para controle de fitopatógenos), uso de plantas antagonistas (plantas que combatem patógenos),
        alqueive (pousio) e outras medidas já mencionadas anteriormente.</li>
     </ul>
